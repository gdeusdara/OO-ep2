package model;

public class Simulacao {
	private float frequenciaAngular;
	private float amplitude;
	private float angFaseSen;
	private int numHarmonicos;
	private int ordemHarmonica;
	private int harmonicoParidade;
	public float getFrequenciaAngular() {
		return frequenciaAngular;
	}
	public void setFrequenciaAngular(float frequenciaAngular) {
		this.frequenciaAngular = frequenciaAngular;
	}
	public float getAmplitude() {
		return amplitude;
	}
	public void setAmplitude(float amplitude) {
		this.amplitude = amplitude;
	}
	public float getAngFaseSen() {
		return angFaseSen;
	}
	public void setAngFaseSen(float angFaseSen) {
		this.angFaseSen = angFaseSen;
	}
	public int getNumHarmonicos() {
		return numHarmonicos;
	}
	public void setNumHarmonicos(int numHarmonicos) {
		this.numHarmonicos = numHarmonicos;
	}
	public int getOrdemHarmonica() {
		return ordemHarmonica;
	}
	public void setOrdemHarmonica(int ordemHarmonica) {
		this.ordemHarmonica = ordemHarmonica;
	}
	public int getHarmonicoParidade() {
		return harmonicoParidade;
	}
	public void setHarmonicoParidade(int harmonicoParidade) {
		this.harmonicoParidade = harmonicoParidade;
	}
	
	
	

}
